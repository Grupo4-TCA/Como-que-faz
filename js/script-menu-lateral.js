function AbrirMenuLateral() {
    var x = document.getElementById("user-menu");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";
    }
  }
  function FecharMenuLateral() {
    var x = document.getElementById("user-menu");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";
    }
  }